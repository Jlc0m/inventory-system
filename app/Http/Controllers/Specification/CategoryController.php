     * @return \Illuminate\Http\Response
     */
    public function store(Category $category, Request $request)
    {
        $request->validate([
            'name' => 'required|min:3|max:50'
        ]);

        $category->create($request->all());

        return redirect()->back()->with('status', 'Successful add category');
    }

